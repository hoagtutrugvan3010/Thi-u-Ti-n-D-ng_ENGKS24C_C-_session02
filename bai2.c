#include<stdio.h>
	int main(){
		
		// khai bao bien 1 2 so nguyen
		int number1 = 10;
		int number2 = 5;
		// tinh tong va luu vao 1 bien khac
		int number3 = number1 + number2;
		
		printf(" tong cua number1 va number 2 la %d\n", number3);
		
		// tao them 3 bien  tich hieu thuong
		int number4 = number1 * number2;
		printf(" tich cua n1 va n2 la %d\n", number4);
		
		int number5 = number1 - number2;
		printf(" hieu cua n1 va n2 la %d\n", number5);
		
		int number6 = number1 / number2;
		printf(" thuong cua n1 va n2 la %d\n", number6);
		
		return 0;
		
	}
